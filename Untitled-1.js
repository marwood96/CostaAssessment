function myFunction() {
    var OurCoffeesPage = document.getElementById("OurCoffeesPage");
    if (OurCoffeesPage.style.display === "none") {
        OurCoffeesPage.style.display = "block";
        HomePage.style.display = "none";
        OurMenuPage.style.display = "none";
        OrderOnlinePage.style.display = "none";
        CostaClubPage.style.display = "none";
        SustainabilityPage.style.display = "none";
        PrivacyPolicyPage.style.display = "none";
        CookiePage.style.display = "none";
        TermsPage.style.display = "none";
        document.getElementById("ourCoffees").disabled = true;
        document.getElementById("ourMenu").disabled = false;
        document.getElementById("costaClub").disabled = false;
        document.getElementById("orderOnline").disabled = false;
        document.getElementById("sustainability").disabled = false;
        document.getElementById("logoclick").disabled = false;
        document.getElementById("privacybutton").disabled = false;
        document.getElementById("cookiebutton").disabled = false;
        document.getElementById("termsbutton").disabled = false;
    } else {
        OurCoffeesPage.style.display = "none";
    }
  }



